# ╔═╡ f8347efd-ef30-43cf-9845-08fe6b13b924
function train(; kws...)
    args = Args(; kws...) # collect options in a struct for convenience

    @info "Getting data..."
    
    train_label, test_label = get_data(args)

    model =define_model() # my implmentation
    # model = alexnet() # Metalhead's implmentation
    # model = LeNet5() # test pipeline


    ps = Flux.params(model)  
    opt = ADAM(args.η) 

    @info "Starting to training..."

    for epoch in 1:args.epochs

        for (x,y) in ProgressBar(train_label)
            @assert size(x) == (64, 64, 3, args.batchsize) 
            @assert size(y) == (10, args.batchsize) 

            gs = gradient(() -> loss_function(model(x), y), ps) 
            Flux.Optimise.update!(opt, ps, gs)
        end

        train = evaluation_loss_accuracy(train_label, model)
        test = evaluation_loss_accuracy(test_label, model)

        println("Epoch $(epoch-1)")
        println("\t Train => loss = $(train[1]) \t acc = $(train[2])")
        println("\t Test => loss = $(test[1]) \t acc = $(test[2])")
    
    end
end

